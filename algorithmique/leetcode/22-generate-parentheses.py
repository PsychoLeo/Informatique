def generateParenthesis(n) :
    if n <= 1 :
        return ['()']
    l = set(generateParenthesis(n-1))
    r = set()
    for x in l :
        r.add('()'+x)
        r.add(x+'()')
        r.add('('+x+')')
    return list(r)

